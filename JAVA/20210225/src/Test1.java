		//[2]정수값을 저장할수 있는 변수 선언
		byte v1; // 1바이트 크기의 정수값을 저장할 v1이라는 이름의 변수 만들기
				//( -128 ~ 127  사이의 값을 저장할 수 있음)
		
		short v2; // 2바이트 크기의 정수값을 저장할 v2라는 이름의 변수 메모리 만들기
