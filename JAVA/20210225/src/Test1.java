		int v3; // 4바이트 크기의 정수값을 저장할 v3라는 이름의 변수 메모리 만들기
				// (-2147483648 ~ 2147483647)
		
		long v4; // 8바이트 크기의 정수값을 저장할 v4라는 이름의 변수 메모리 만들기
