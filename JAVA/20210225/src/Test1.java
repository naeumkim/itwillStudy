		// [3] 문자형(한개의 영문자, 숫자, 특수문자)데이터를 저장할 변수 선언
		char c; //'a', 'A', '1', 'j', '_'
		
		//2. 위 1.에서 선언한(만든) 변수 메모리에 데이터를 저장(변수의 값을 초기화)
		//변수메모리에 값 저장
		//문법
		//변수명 = 저장할 값;
		
